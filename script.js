document.addEventListener('DOMContentLoaded', () => {
    // 1. Utilities Setup
    let currentLang = 'en';
    let currentTheme = localStorage.getItem('theme') || 'light';
    
    // Set initial Year
    document.getElementById('currentYear').textContent = new Date().getFullYear();

    // 2. Preloader & Initial Reveal
    const preloader = document.getElementById('preloader');
    
    window.addEventListener('load', () => {
        setTimeout(() => {
            preloader.classList.add('hidden');
            // Trigger number counters after preloader is hidden
            setTimeout(initCounters, 800);
        }, 1000);
    });

    // 3. Theme Toggle
    const themeToggleBtn = document.getElementById('themeToggle');
    
    function setTheme(theme) {
        document.documentElement.setAttribute('data-theme', theme);
        localStorage.setItem('theme', theme);
        currentTheme = theme;
    }
    
    // Apply initialized theme
    setTheme(currentTheme);

    themeToggleBtn.addEventListener('click', () => {
        const newTheme = document.documentElement.getAttribute('data-theme') === 'light' ? 'dark' : 'light';
        setTheme(newTheme);
    });

    // 4. Internationalization (I18n)
    const langToggleBtn = document.getElementById('langToggle');
    const langLabel = document.getElementById('langLabel');

    function updateLanguage(newLang) {
        currentLang = newLang;
        langLabel.textContent = currentLang === 'en' ? 'ES' : 'EN';
        
        const i18nElements = document.querySelectorAll('[data-i18n]');
        i18nElements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[currentLang][key]) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    if (el.placeholder) el.placeholder = translations[currentLang][key];
                } else if (el.tagName === 'LABEL') {
                    el.textContent = translations[currentLang][key];
                } else {
                    el.textContent = translations[currentLang][key];
                }
            }
        });
    }

    langToggleBtn.addEventListener('click', () => {
        const toggleTarget = currentLang === 'en' ? 'es' : 'en';
        updateLanguage(toggleTarget);
    });

    // 5. Navigation & Mobile Menu
    const navbar = document.getElementById('navbar');
    const menuToggle = document.getElementById('menuToggle');
    const navLinks = document.getElementById('navLinks');
    const navItems = document.querySelectorAll('.nav-links a');

    // Scroll effect for navbar
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // Mobile menu toggle
    menuToggle.addEventListener('click', () => {
        if (window.innerWidth <= 768) {
            navLinks.classList.toggle('active');
            menuToggle.classList.toggle('menu-open');
        }
    });

    // Close mobile menu on click & Smooth Scroll
    navItems.forEach(item => {
        item.addEventListener('click', function(e) {
            if (this.getAttribute('href').startsWith('#')) {
                e.preventDefault();
                const targetId = this.getAttribute('href').substring(1);
                const targetEl = document.getElementById(targetId);
                
                if (targetEl) {
                    window.scrollTo({
                        top: targetEl.offsetTop - 70, // offset for fixed header
                        behavior: 'smooth'
                    });
                }
                
                if (window.innerWidth <= 768) {
                    navLinks.classList.remove('active');
                    menuToggle.classList.remove('menu-open');
                }
            }
        });
    });

    // Active Navigation Highlight on Scroll
    window.addEventListener('scroll', () => {
        let current = '';
        const sections = document.querySelectorAll('.section');
        
        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;
            if (pageYOffset >= (sectionTop - 200)) {
                current = section.getAttribute('id');
            }
        });

        navItems.forEach(item => {
            item.classList.remove('active');
            if (item.getAttribute('href').includes(current)) {
                item.classList.add('active');
            }
        });
    });

    // 6. Intersection Observer for Scroll Animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                // Optional: Stop observing once revealed
                // observer.unobserve(entry.target); 
            }
        });
    }, observerOptions);

    const revealElements = document.querySelectorAll('.reveal-up, .reveal-left, .reveal-right');
    revealElements.forEach(el => {
        observer.observe(el);
    });

    // 7. Back to Top Button + Floating Logo
    const backToTopBtn = document.getElementById('backToTop');
    const floatingLogo = document.getElementById('floatingLogo');
    
    window.addEventListener('scroll', () => {
        if (window.scrollY > 500) {
            backToTopBtn.classList.add('visible');
        } else {
            backToTopBtn.classList.remove('visible');
        }
        // Floating logo: show exactly when navbar logo fades out (scroll > 50px)
        if (window.scrollY > 50) {
            floatingLogo && floatingLogo.classList.add('visible');
        } else {
            floatingLogo && floatingLogo.classList.remove('visible');
        }
    });

    backToTopBtn.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });

    // 8. Number Counters
    function initCounters() {
        const counters = document.querySelectorAll('.stat-number');
        counters.forEach(counter => {
            const target = +counter.getAttribute('data-count');
            const duration = 2000;
            const step = Math.ceil(target / (duration / 16)); // assuming 60fps
            
            let current = 0;
            const updateCounter = () => {
                current += step;
                if (current < target) {
                    counter.innerText = current;
                    requestAnimationFrame(updateCounter);
                } else {
                    counter.innerText = target;
                }
            };
            requestAnimationFrame(updateCounter);
        });
    }

    // 9. Contact form submit handler (Prevent default logic)
    const contactForm = document.getElementById('contactForm');
    if (contactForm) {
        contactForm.addEventListener('submit', (e) => {
            e.preventDefault();
            // Just visually pretend message sent for this demo
            const btn = contactForm.querySelector('button[type="submit"]');
            const originalText = btn.textContent;
            
            btn.innerHTML = '✓ Sent / Enviado';
            btn.style.backgroundColor = 'var(--accent-green)';
            contactForm.reset();
            
            setTimeout(() => {
                btn.innerHTML = originalText;
                btn.style.backgroundColor = '';
            }, 3000);
        });
    }
});
